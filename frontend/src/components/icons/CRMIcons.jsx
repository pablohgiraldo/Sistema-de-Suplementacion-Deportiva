// Función helper para obtener el icono según el segmento
export const getSegmentIcon = (segment, className = "w-8 h-8") => {
    const icons = {
        'VIP': <DiamondIcon className={className} />,
        'Frecuente': <RepeatIcon className={className} />,
        'Ocasional': <CalendarIcon className={className} />,
        'Nuevo': <SparklesIcon className={className} />,
        'En Riesgo': <AlertIcon className={className} />,
        'Inactivo': <SleepIcon className={className} />
    };
    return icons[segment] || <UserIcon className={className} />;
};

